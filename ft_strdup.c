#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*tab;
	int		i;
	int		len;

	if (s1 == NULL)
		return (NULL);
	len = ft_strlen(s1);
	tab = (char *)malloc(len + 1);
	if (!tab)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		tab[i] = s1[i];
		i++;
	}
	tab[i] = '\0';
	return (tab);
}
